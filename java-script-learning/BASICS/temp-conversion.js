let fahr = 50;
console.log("Fahrenheit Temperature: " + fahr);

// Calculate Celsius and store in Celsius variable
// print that value
let celsius = (fahr - 32) * 5/9;
console.log("Celsius Temperature: " + celsius);

// Calculate Kelvin and store in Kelvin variable
// print that value
let kelvin = (fahr + 459.67)* 5/9;
console.log("Kelvin Temperature: " + kelvin);